public class DataTypes {
    public static void main(String[] args) {
        // Declare and initialize primitive data types
        int a = 25;
        float b = 5.9f;
        double c = 70.5;
        char d = 'A';
        boolean e = true;

        // Display the values
        System.out.println("Integer (int): " + a);
        System.out.println("Floating-point (float): " + b);
        System.out.println("Double-precision (double): " + c);
        System.out.println("Character (char): " + d);
        System.out.println("Boolean (boolean): " + e);
    }
}
